v = input("Digite alguma coisa para verificar seu tipo primitivo e outras informações:")
print("O tipo primitivo é ", type(v))
print("Isso é Alfanumérico ?", v.isalnum())
print("São apenas numeros ?", v.isnumeric())
print("São somente letras ?", v.isalpha())
print("São somente espaços ?", v.isspace())
print("São decimais ?", v.isdecimal())
print("Estão em Maiusculo ?", v.isupper())
print("Estão em minisculo ?", v.islower())
print("Estão captalizadas ?", v.istitle())






